void USTCGameInstance::LoadGameData()
{
	if (!UGameplayStatics::DoesSaveGameExist(SaveSlotName, 0))
	{
		UE_LOG(LogTemp, Log, TEXT("[STCGameInstance] No save file found - skip load"));
		return;
	}

	CachedSave = Cast<UGameSaveSystem>(UGameplayStatics::LoadGameFromSlot(SaveSlotName, 0));
	if (!CachedSave)
	{
		UE_LOG(LogTemp, Warning, TEXT("[STCGameInstance] LoadGameData: Cast failed"));
		return;
	}

	// п§ўл·Җлөҫ???ләЈлӮ« и«ӣкі лЈ·
	if (UPartyInstance* PlayerSub = GetSubsystem<UPartyInstance>())
	{
		PlayerSub->SetPartyInfo(CachedSave->SavedChampionsInfo);
	}

	// п§Қ??ләЈлӮ« и«ӣкі лЈ·
	if (URunSystem* MapSub = GetSubsystem<URunSystem>())
	{
		MapSub->SetMapInfo(CachedSave->SavedMapInfo);
	}

	UE_LOG(LogTemp, Log, TEXT("[STCGameInstance] LoadGameData completed"));
}

bool USTCGameInstance::HasSaveGameData() const
{
	return UGameplayStatics::DoesSaveGameExist(SaveSlotName, 0);
}

bool USTCGameInstance::DeleteSaveGameData()
{
	CachedSave = nullptr;

	if (!UGameplayStatics::DoesSaveGameExist(SaveSlotName, 0))
	{
		UE_LOG(LogTemp, Log, TEXT("[STCGameInstance] DeleteSaveGameData skipped. Save file does not exist."));
		return true;
	}

	const bool bDeleted = UGameplayStatics::DeleteGameInSlot(SaveSlotName, 0);
	UE_LOG(LogTemp, Log, TEXT("[STCGameInstance] DeleteSaveGameData %s"), bDeleted ? TEXT("completed") : TEXT("failed"));
	return bDeleted;
}

